<h1>Track Info</h1>
<p>Our home track, Charlotte Motor Speedway, has a very important part of NASCAR history. Built in 1960, the track is still running strong more than fifty years later, with multiple NASCAR races hosted here each year. Despite the name, the track is located in Concord, North Carolina.<br />
We use three of the tracks that Charlotte Motor Speedway hosts:</p>

<h2><a name="oval">Oval Course</a></h2>
<img src="images/charOval.png" alt="Charlotte Oval Course" height="474" width="668" />
<p>The oval course consists of 4 turns with 24 degrees banking. It is 1.5 miles long, in what is commonly known as the "cookie cutter" shape since it is very similar to many other ovals on the NASCAR circuit.</p>
<br />

<h3><a name="fullRoad">Full Road Course</a></h3>
<img src="images/charFullRoad.png" alt="Charlotte Full Road Course" height="474" width="668" />
<p>The full road course consists of 12 turns with a very wide variety of turn types. It also connects to the oval course, allowing you to get up to higher speeds on your laps. The course is 2.25 miles.</p>

<br />
<h3><a name="infieldRoad">Infield Road Course</a></h3>
<img src="images/charInfieldRoad.png" alt="Charlotte Infield Road Course" height="474" width="668" />
<p>The infield road course consists of 12 turns, just like the full road course. The shorter distance, only .73 miles, means that their are many low speed turns. This track may not be fast, but it is a great test of driving abilities on the sharper turns.</p>


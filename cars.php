<h1>Car Info</h1>
<p>In an attempt to provide you with the widest selection of driving types, we have selected four cars that give you a variety wide enough to get the experience you want and deserve:</p>

<h2><a name="mazda">Mazda MX-5 Cup</a></h2>
<img src="images/mx5.jpg" alt="Mazda MX-5 Cup" height="379" width="670" />
<p>The Mazda MX-5 Cup is a great all around car. It may not be the fastest car on the road, but it has superb handling. The MX5 has a 2.0 Liter engine, allowing 170hp with a 6-Speed manual transmission. It weighs 2600 pounds, meaning it's fairly light, and fun to throw around corners. The weight to power ratio of this car is 15.2.</p>
<br />

<h2><a name="skip">Skip Barber</a></h2>
<img src="images/skip.jpg" alt="Skip Barber" height="379" width="670" />
<p>The Skip Barber Formula 2000 contains a 2.0 Liter, 150hp, engine. That might be less power than the MX5, but don't let that fool you. The lighter chassis, only weighing 1250 pounds is so light, it can get away from you quickly. This car requires a steady foot and extreme focus to keep it pointed the way you want. This car is considered to be more advanced than the MX5. It has a weight to power ration of 9.4.</p>
<br />

<h2><a name="streetStock">Street Stock</a></h2>
<img src="images/streetStock.jpg" alt="Street Stock" height="379" width="670" />
<p>The Street Stock is a great introduction to oval racing. The lower power allows you to get more comfortable with the track, learning the lines necessary to turn the fastest lap around our 1.5 mile oval course. Holding a 375hp engine, and weighing 3400 pounds, the Street Stock is easy to drive, but difficult to master. For special events, this car is also a blast on the road course. The weight to power ratio is 9.0.</p>
<br />

<h2><a name="fordFusion">Ford Fusion Stock Car</a></h2>
    <img src="images/stockCar.jpg" alt="Ford Fusion Stock Car" height="379" width="670" />
<p>The Ford Fusion Stock Car is the same car that visits this track many times a year as part of the NASCAR Sprint Cup Series. It is the top of the food chain, packed with more than 875hp, and weighing only 3300 pounds, this car is a monster. It will take everything you've learned to even turn laps. This car is an advanced car and requires drivers to prove that they are capable of turning consistent laps in the Street Stock. The weight to power ratio is an amazing 3.7.</p>


